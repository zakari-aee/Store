export const mockProducts = Object.values(categoryProducts).flat();

// Helper function to get products by category
export const getProductsByCategory = (category) => {
  return categoryProducts[category] || [];
};

// Helper function to get featured products (first 2 from each category)
export const getFeaturedProducts = () => {
  return Object.values(categoryProducts).flatMap(products => products.slice(0, 2));
};

// Helper function to get new products
export const getNewProducts = () => {
  return mockProducts.filter(product => product.isNew);
};